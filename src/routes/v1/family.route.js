const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const familyValidation = require('../../validations/family.validation');
const familyController = require('../../controllers/family.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageUsers'), validate(familyValidation.createFamily), familyController.createFamily)
  .get(auth('manageUsers'), validate(familyValidation.getFamilies), familyController.getFamilies);

router
  .route('/:familyId')
  .get(auth('manageUsers'), validate(familyValidation.getFamily), familyController.getFamily)
  .delete(auth('manageUsers'), validate(familyValidation.deleteFamily), familyController.deleteFamily);

module.exports = router;

